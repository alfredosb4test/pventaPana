function sanear_string_log($string){
	$string = trim($string);
  //Esta parte se encarga de eliminar cualquier caracter extraño
    $string = str_replace(
        array("\\", "¨", "º", "-", "~","#", "|", "^", "`", "¨", "´","-","*"),'',$string);	
    $string = str_replace("'",'&prime;',$string);	

 
    $string = str_replace(
        array('á', 'à', 'ä', 'â', 'ª', 'Á', 'À', 'Â', 'Ä'),
        array('&aacute;', '&agrave;', '&auml;', '&acirc;', 'a', '&Aacute;', '&Agrave;', '&Acirc;', '&Auml;'),
        $string
    );
 
    $string = str_replace(
        array('é', 'è', 'ë', 'ê', 'É', 'È', 'Ê', 'Ë'),
        array('&eacute;', '&egrave;', '&euml;', '&ecirc;', '&Eacute;', '&Egrave;', '&Ecirc;', '&Euml;'),
        $string
    );
 
    $string = str_replace(
        array('í', 'ì', 'ï', 'î', 'Í', 'Ì', 'Ï', 'Î'),
        array('&iacute;', '&igrave;', '&iuml;', '&icirc;', '&Iacute;', '&Igrave;', '&Icirc;', '&Iuml;'),
        $string
    );
 
    $string = str_replace(
        array('ó', 'ò', 'ö', 'ô', 'Ó', 'Ò', 'Ö', 'Ô'),
        array('&oacute;', '&ograve;', '&ouml;', '&ocirc;', '&Oacute;', '&Ograve;', '&Ocirc;', '&Ouml;'),
        $string
    );
 
    $string = str_replace(
        array('ú', 'ù', 'ü', 'û', 'Ú', 'Ù', 'Û', 'Ü'),
        array('&uacute;', '&ugrave;', '&uuml;', '&ucirc;', '&Uacute;', '&Ugrave;', '&Ucirc;', '&Uuml;'),
        $string
    );
 
    $string = str_replace(
        array('ñ', 'Ñ', 'ç', 'Ç'),
        array('&ntilde;', '&Ntilde;', 'c', 'C'),
        $string
    );
	
    $string = str_replace(
        array('¿', '?'),
        array('&#191;', '&#63;'),
        $string
    );	
	 return $string;
}
